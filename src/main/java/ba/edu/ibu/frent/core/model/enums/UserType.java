package ba.edu.ibu.frent.core.model.enums;

/**
 * Enumeration representing different user types in the system.
 * Each constant represents a specific user type, such as member, employee, or admin.
 */
public enum UserType {

    /**
     * Represents a regular member user type.
     */
    MEMBER,

    /**
     * Represents an employee user type.
     */
    EMPLOYEE,

    /**
     * Represents an administrator user type with elevated privileges.
     */
    ADMIN
}

